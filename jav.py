# 1. Print 1-255
# Print1To255()
# Print all the integers from 1 to 255. 

for print1to255 in range (1,255 + 1):
    print(print1to255)


# 2. Print Odds 1-255
# PrintOdds1To255()
# Print all odd integers from 1 to 255. 

for print1to255 in range (1,255 + 1):
  if print1to255 % 2 != 0:
    print(print1to255, end = " ")


